function template_rules = load_template_rules()
    % Load predefined template rules for common database performance issues
    template_rules = struct();
    
    % CPU-bound rules
    template_rules.cpu_bound = struct(...
        'metrics', {'cpu_usage', 'io_wait'},...
        'pattern', 'cpu_usage > 80 AND io_wait < 20',...
        'confidence', 0.8...
    );
    
    % IO-bound rules
    template_rules.io_bound = struct(...
        'metrics', {'io_wait', 'disk_queue_length'},...
        'pattern', 'io_wait > 40 OR disk_queue_length > 10',...
        'confidence', 0.7...
    );
    
    % Memory pressure rules
    template_rules.memory_pressure = struct(...
        'metrics', {'memory_usage', 'swap_usage'},...
        'pattern', 'memory_usage > 90 OR swap_usage > 20',...
        'confidence', 0.75...
    );
end 